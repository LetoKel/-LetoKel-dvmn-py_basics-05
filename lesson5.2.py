RESULT_DIR = "result"
NUMBER_OF_CHARACTERS = 10


def generate_character_skills(skills_list, count=3):
    return random.sample(skills_list, count)


def convert_to_runic(text, alphabet):
    runic_text = text
    for original_letter, runic_letter in alphabet.items():
        runic_text = runic_text.replace(original_letter, runic_letter)
    return runic_text


def create_character_context(fake):
    character_skills = generate_character_skills(SKILLS, 3)
    runic_skills = [convert_to_runic(skill, RUNIC_ALPHABET) for skill in character_skills]

    return {
        "first_name": fake.first_name(),
        "last_name": fake.last_name(),
        "job": fake.job(),
        "town": fake.city(),
        "strength": random.randint(3, 18),
        "agility": random.randint(3, 18),
        "endurance": random.randint(3, 18),
        "intelligence": random.randint(3, 18),
        "luck": random.randint(3, 18),
        "skill_1": runic_skills[0],
        "skill_2": runic_skills[1],
        "skill_3": runic_skills[2],
    }


def save_character_card(card_number, context):
    os.makedirs(RESULT_DIR, mode=0o777, exist_ok=True)
    result_card_name = os.path.join(RESULT_DIR, f"result{card_number}.svg")
    file_operations.render_template("charsheet.svg", result_card_name, context)


def main():
    fake = Faker("ru_RU")

    for number in range(1, NUMBER_OF_CHARACTERS + 1):
        context = create_character_context(fake)
        save_character_card(number, context)


if __name__ == "__main__":
    main()

    